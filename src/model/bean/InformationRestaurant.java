package model.bean;

public class InformationRestaurant {
	private String phone;
	private String describe;
	private String timeOpenInWeek;
	private String timeOpenWeekend;
	
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getDescribe() {
		return describe;
	}
	public void setDescribe(String describe) {
		this.describe = describe;
	}
	public String getTimeOpenInWeek() {
		return timeOpenInWeek;
	}
	public void setTimeOpenInWeek(String timeOpenInWeek) {
		this.timeOpenInWeek = timeOpenInWeek;
	}
	public String getTimeOpenWeekend() {
		return timeOpenWeekend;
	}
	public void setTimeOpenWeekend(String timeOpenWeekend) {
		this.timeOpenWeekend = timeOpenWeekend;
	}
	
}
